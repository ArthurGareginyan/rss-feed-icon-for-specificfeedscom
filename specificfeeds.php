defined('ABSPATH') or die("Restricted access!");

/**
 * Register text domain
 *
 * @since 0.1
 */
function specificfeeds_textdomain() {
	load_plugin_textdomain( 'specificfeeds', false, dirname( plugin_basename( __FILE__ ) ) . '/languages/' );
}
add_action( 'init', 'specificfeeds_textdomain' );

/**
 * Print direct link to SpecificFeeds admin page
 *
 * Fetches array of links generated by WP Plugin admin page ( Deactivate | Edit )
 * and inserts a link to the SpecificFeeds admin page
 *
 * @since  0.1
 * @param  array $links Array of links generated by WP in Plugin Admin page.
 * @return array        Array of links to be output on Plugin Admin page.
 */
function specificfeeds_settings_link( $links ) {
	$settings_page = '<a href="' . admin_url( 'options-general.php?page=settings_page.php' ) .'">' . __( 'Settings', 'specificfeeds' ) . '</a>';
	array_unshift( $links, $settings_page );
	return $links;
}
$plugin = plugin_basename( __FILE__ );
add_filter( "plugin_action_links_$plugin", 'specificfeeds_settings_link' );

/**
 * Register "SpecificFeeds" submenu in "Settings" Admin Menu
 *
 * @since 0.1
 */
function specificfeeds_menu() {
	add_options_page('SpecificFeeds', 'SpecificFeeds', 'manage_options', 'settings_page.php', 'specificfeeds_render_submenu_page');
}
add_action('admin_menu', 'specificfeeds_menu');

/**
 * Attach Settings Page
 *
 * @since 0.2
 */
require_once( plugin_dir_path( __FILE__ ) . 'inc/settings_page.php' );

/**
 * Register settings
 *
 * @since 0.1
 */
function specificfeeds_register_settings() {
	register_setting( 'specificfeeds_settings_group', 'specificfeeds_link' );
	register_setting( 'specificfeeds_settings_group', 'specificfeeds_icon' );
}
add_action( 'admin_init', 'specificfeeds_register_settings' );

/**
 * ShortCode SpecificFeeds
 *
 * @since 1.0
 */
function specificfeeds_shortcode() {


    // Set variables
    $sf_link = get_option( 'specificfeeds_link' );
    $sf_icon = get_option( 'specificfeeds_icon' );
    
	// Generating output code
	return '<a
			href="'.$sf_link.'"
			target="_balnk"
			rel="nofollow"
    		>
    		<img
    			src="http://www.specificfeeds.com/theme/classic/img/publisher/download/'.$sf_icon.'_one.png"
    			width="48"
    			height="48"
    			style="border: none;"
    		/>
    		</a>';
}
add_shortcode('specificfeeds', 'specificfeeds_shortcode');

/**
 * Delete Options on Uninstall
 *
 * @since 0.1
 */
function specificfeeds_uninstall() {
	delete_option( 'specificfeeds_link' );
	delete_option( 'specificfeeds_icon' );
}
register_uninstall_hook( __FILE__, 'specificfeeds_uninstall' );


